<h5><b>8. Course requirements and expectations: </b></h5>
<!----------------------------------8. Các quy định chung --------------------------->
<h6><b>8.1 Requirements on attendance</b></h6>
<ul>
    <li>Students are responsible for attending all classes. In case of absence due to force majeure circumstances, there
        must be sufficient and reasonable evidence.</li>
    <li>Students who do not attend more than 20% of the class sections, whether for reason or not, are deemed not to
        have completed the course and must re-enroll in the following semester.</li>
</ul>
<h6><b>8.2 Requirements and expectations on student behaviors </b></h6>
<ul>
    <li>Students must show their respects for teachers and other learners.</li>
    <li>Students must be on time. Students who are late more than five minutes will not be allowed to attend the class.
    </li>
    <li>Students should not make noise and interfere with others in the learning process.</li>
    <li>Students should not eat, chew gum, and use devices such as cell phones, music players during class hours.</li>
    <li>Laptops and tablets can only be used in class for the purpose of learning. </li>
    <li>Students who violate the above principles will be asked to leave the class and considered absent from the class.
    </li>
</ul>
<h6><b>8.3 Requirements on learning issues</b></h6>
<p>Issues related to applying for score reservation, scoring complaints, scoring, exam disciplines are done according to
    the Learning Regulation of Tra Vinh University.</p>
